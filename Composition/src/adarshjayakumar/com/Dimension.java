public class Dimension {
    private int lenght;
    private int height;
    private int width;

    public Dimension(int lenght, int height, int width) {
        this.lenght = lenght;
        this.height = height;
        this.width = width;
    }

    public int getLenght() {
        return lenght;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
