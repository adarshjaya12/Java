public class Size {
    private int lenght;
    private int breath;
    private int height;

    public Size(int lenght, int breath, int height) {
        this.lenght = lenght;
        this.breath = breath;
        this.height = height;
    }

    public int getLenght() {
        return lenght;
    }

    public int getBreath() {
        return breath;
    }

    public int getHeight() {
        return height;
    }
}
